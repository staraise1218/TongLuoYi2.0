package com.tly.model;


/**
 * 管理员
 * @ClassName Administrator
 * @Author yangweihang
 * @Date 2018年8月24日 上午11:23:09
 */
public class Administrator {
	private Integer id;//id
	private String wxid;//管理员微信id
	private String name;//管理员姓名
	private String phonenum;//管理员电话
	private Integer sid;//店铺id
	private Integer type;//类型 1.管理员 2.店员
	public Administrator() {
	}
	public Administrator(Integer id, String wxid, String name, String phonenum, Integer sid, Integer type) {
		this.id = id;
		this.wxid = wxid;
		this.name = name;
		this.phonenum = phonenum;
		this.sid = sid;
		this.type = type;
	}
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getWxid() {
		return wxid;
	}
	public void setWxid(String wxid) {
		this.wxid = wxid;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPhonenum() {
		return phonenum;
	}
	public void setPhonenum(String phonenum) {
		this.phonenum = phonenum;
	}
	public Integer getSid() {
		return sid;
	}
	public void setSid(Integer sid) {
		this.sid = sid;
	}
	public Integer getType() {
		return type;
	}
	public void setType(Integer type) {
		this.type = type;
	}
	@Override
	public String toString() {
		return "Administrator [id=" + id + ", wxid=" + wxid + ", name=" + name + ", phonenum=" + phonenum + ", sid="
				+ sid + "]";
	}
}
