package com.tly.model;

import java.util.Date;

/**
 * 故障设备维修明细
 * @ClassName EquipmentMaintenance
 * @Author yangweihang
 * @Date 2018年8月26日 下午11:30:17
 */
public class EquipmentMaintenance {
	private Integer emid;//id
	private Integer pid;//合伙人id
	private Integer wid;//维修人员id
	private String lxname;//联系人电话
	private String lxphonenum;//联系人电话
	private Integer eid;//设备id
	private String gzdescribe;//故障描述
	private String judge;//工程师判断
	private String record;//维修记录
	private String result;//维修结果
	private Date startime;//维修开始时间
	private Date endtime;//维修结束时间
	private Integer look;//1.已读 2.未读
	public EquipmentMaintenance() {
	}
	public EquipmentMaintenance(Integer emid, Integer pid, Integer wid, String lxname, String lxphonenum, Integer eid,
			String gzdescribe, String judge, String record, String result, Date startime, Date endtime, Integer look) {
		this.emid = emid;
		this.pid = pid;
		this.wid = wid;
		this.lxname = lxname;
		this.lxphonenum = lxphonenum;
		this.eid = eid;
		this.gzdescribe = gzdescribe;
		this.judge = judge;
		this.record = record;
		this.result = result;
		this.startime = startime;
		this.endtime = endtime;
		this.look = look;
	}
	public Integer getEmid() {
		return emid;
	}
	public void setEmid(Integer emid) {
		this.emid = emid;
	}
	public Integer getPid() {
		return pid;
	}
	public void setPid(Integer pid) {
		this.pid = pid;
	}
	public Integer getWid() {
		return wid;
	}
	public void setWid(Integer wid) {
		this.wid = wid;
	}
	public String getLxname() {
		return lxname;
	}
	public void setLxname(String lxname) {
		this.lxname = lxname;
	}
	public String getLxphonenum() {
		return lxphonenum;
	}
	public void setLxphonenum(String lxphonenum) {
		this.lxphonenum = lxphonenum;
	}
	public Integer getEid() {
		return eid;
	}
	public void setEid(Integer eid) {
		this.eid = eid;
	}
	public String getGzdescribe() {
		return gzdescribe;
	}
	public void setGzdescribe(String gzdescribe) {
		this.gzdescribe = gzdescribe;
	}
	public String getJudge() {
		return judge;
	}
	public void setJudge(String judge) {
		this.judge = judge;
	}
	public String getRecord() {
		return record;
	}
	public void setRecord(String record) {
		this.record = record;
	}
	public String getResult() {
		return result;
	}
	public void setResult(String result) {
		this.result = result;
	}
	public Date getStartime() {
		return startime;
	}
	public void setStartime(Date startime) {
		this.startime = startime;
	}
	public Date getEndtime() {
		return endtime;
	}
	public void setEndtime(Date endtime) {
		this.endtime = endtime;
	}
	public Integer getLook() {
		return look;
	}
	public void setLook(Integer look) {
		this.look = look;
	}
	@Override
	public String toString() {
		return "EquipmentMaintenance [emid=" + emid + ", pid=" + pid + ", wid=" + wid + ", lxname=" + lxname
				+ ", lxphonenum=" + lxphonenum + ", eid=" + eid + ", gzdescribe=" + gzdescribe + ", judge=" + judge
				+ ", record=" + record + ", result=" + result + ", startime=" + startime + ", endtime=" + endtime
				+ ", look=" + look + "]";
	}
}
