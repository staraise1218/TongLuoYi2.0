package com.tly.model;

import java.util.Date;

/**
 * 会员
 * @ClassName Member
 * @Author yangweihang
 * @Date 2018年8月24日 上午11:24:35
 */
public class Member {
	private Integer id;//会员id
	private String wxid;//会员微信id
	private String nickname;//会员昵称
	private String name;//会员姓名
	private int sex;//性别
	private String date;//出生日期
	private String phonenum;//手机号码
	private String city;//城市
	private int mlid;//会员级别id 1.普通 2.金牌 3.钻石 4.超级
	private int type;//会员类型 1.会员 2.合伙人 3.管理员 4.维修工程师
	private String img;//会员头像
	private String mewm;//二维码
	private Date jrtime;//加入时间
	public Member() {
	}
	public Member(Integer id, String wxid, String nickname, String name, int sex, String date, String phonenum,
			String city, int mlid, int type, String img, String mewm, Date jrtime) {
		this.id = id;
		this.wxid = wxid;
		this.nickname = nickname;
		this.name = name;
		this.sex = sex;
		this.date = date;
		this.phonenum = phonenum;
		this.city = city;
		this.mlid = mlid;
		this.type = type;
		this.img = img;
		this.mewm = mewm;
		this.date = date;
	}
	public Date getJrtime() {
		return jrtime;
	}
	public void setJrtime(Date jrtime) {
		this.jrtime = jrtime;
	}
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getWxid() {
		return wxid;
	}
	public void setWxid(String wxid) {
		this.wxid = wxid;
	}
	public String getNickname() {
		return nickname;
	}
	public void setNickname(String nickname) {
		this.nickname = nickname;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getSex() {
		return sex;
	}
	public void setSex(int sex) {
		this.sex = sex;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getPhonenum() {
		return phonenum;
	}
	public void setPhonenum(String phonenum) {
		this.phonenum = phonenum;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public int getMlid() {
		return mlid;
	}
	public void setMlid(int mlid) {
		this.mlid = mlid;
	}
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
	public String getImg() {
		return img;
	}
	public void setImg(String img) {
		this.img = img;
	}
	public String getMewm() {
		return mewm;
	}
	public void setMewm(String mewm) {
		this.mewm = mewm;
	}
	@Override
	public String toString() {
		return "Member [id=" + id + ", wxid=" + wxid + ", nickname=" + nickname + ", name=" + name + ", sex=" + sex
				+ ", date=" + date + ", phonenum=" + phonenum + ", city=" + city + ", mlid=" + mlid + ", type=" + type
				+ ", img=" + img + ", mewm=" + mewm + ", jrtime=" + jrtime + "]";
	}
}
