package com.tly.model;


/**
 * 店铺
 * @ClassName Shop
 * @Author yangweihang
 * @Date 2018年8月24日 上午11:26:09
 */
public class Shop {
	private Integer sid;//id
	private Integer pid;//合伙人id
	private String name;//名称
	private String address;//地址
	private String location;//地理经纬度
	private String introduce;//商家介绍
	private String image;//图文页面
	public Shop() {
	}
	public Shop(Integer sid, Integer pid, String name, String introduce, String image, String address, String location) {
		this.sid = sid;
		this.pid = pid;
		this.name = name;
		this.address = address;
		this.location = location;
		this.introduce = introduce;
		this.image = image;
	}
	public Integer getSid() {
		return sid;
	}
	public void setSid(Integer sid) {
		this.sid = sid;
	}
	public Integer getPid() {
		return pid;
	}
	public void setPid(Integer pid) {
		this.pid = pid;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public String getIntroduce() {
		return introduce;
	}
	public void setIntroduce(String introduce) {
		this.introduce = introduce;
	}
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
	@Override
	public String toString() {
		return "Shop [sid=" + sid + ", pid=" + pid + ", name=" + name + ", address=" + address + ", location="
				+ location + ", introduce=" + introduce + ", image=" + image + "]";
	}
}
