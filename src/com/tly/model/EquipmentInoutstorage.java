package com.tly.model;

import java.util.Date;


/**
 * 设备出入库明细
 * @ClassName EquipmentInoutstorage
 * @Author yangweihang
 * @Date 2018年8月24日 上午11:23:40
 */
public class EquipmentInoutstorage {
	private Integer id;//id
	private String number;//设备编号
	private Integer status;//库存状态 1.入库 2.出库 3.报废
	private Date entrytime;//入库时间
	private Date outtime;//出库时间
	private Integer source;//设备来源 1.新品  2.退组  3.修复
	public EquipmentInoutstorage() {
	}
	public EquipmentInoutstorage(Integer id, String number, Integer status, Date entrytime, Date outtime,
			Integer source) {
		this.id = id;
		this.number = number;
		this.status = status;
		this.entrytime = entrytime;
		this.outtime = outtime;
		this.source = source;
	}
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getNumber() {
		return number;
	}
	public void setNumber(String number) {
		this.number = number;
	}
	public Integer getStatus() {
		return status;
	}
	public void setStatus(Integer status) {
		this.status = status;
	}
	public Date getEntrytime() {
		return entrytime;
	}
	public void setEntrytime(Date entrytime) {
		this.entrytime = entrytime;
	}
	public Date getOuttime() {
		return outtime;
	}
	public void setOuttime(Date outtime) {
		this.outtime = outtime;
	}
	public Integer getSource() {
		return source;
	}
	public void setSource(Integer source) {
		this.source = source;
	}
	@Override
	public String toString() {
		return "EquipmentInoutstorage [id=" + id + ", number=" + number + ", status=" + status + ", entrytime="
				+ entrytime + ", outtime=" + outtime + ", source=" + source + "]";
	}
}
