package com.tly.model;

import java.util.Date;


/**
 * 会员使用统计
 * @ClassName UserStatistics
 * @Author yangweihang
 * @Date 2018年8月24日 上午11:26:19
 */
public class UserStatistics {
	private Integer id;//id
	private Integer mid;//会员id
	private Integer eid;//设备子表id
	private Date starttime;//开机时间
	private Long utilitytime;//使用时长
	private Date endtime;//结束时间
	private Double money;//价格
	private Integer look;//管理员1.已读 2.未读
	private Integer plook;//合伙人1.已读 2.未读
	public UserStatistics() {
	}
	public UserStatistics(Integer id, Integer mid, Integer eid, Date starttime, Long utilitytime, Date endtime,
			Double money, Integer look, Integer plook) {
		this.id = id;
		this.mid = mid;
		this.eid = eid;
		this.starttime = starttime;
		this.utilitytime = utilitytime;
		this.endtime = endtime;
		this.money = money;
		this.look = look;
		this.plook = plook;
	}
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getMid() {
		return mid;
	}
	public void setMid(Integer mid) {
		this.mid = mid;
	}
	public Integer getEid() {
		return eid;
	}
	public void setEid(Integer eid) {
		this.eid = eid;
	}
	public Date getStarttime() {
		return starttime;
	}
	public void setStarttime(Date starttime) {
		this.starttime = starttime;
	}
	public Long getUtilitytime() {
		return utilitytime;
	}
	public void setUtilitytime(Long utilitytime) {
		this.utilitytime = utilitytime;
	}
	public Date getEndtime() {
		return endtime;
	}
	public void setEndtime(Date endtime) {
		this.endtime = endtime;
	}
	public Double getMoney() {
		return money;
	}
	public void setMoney(Double money) {
		this.money = money;
	}
	public Integer getLook() {
		return look;
	}
	public void setLook(Integer look) {
		this.look = look;
	}
	public Integer getPlook() {
		return plook;
	}
	public void setPlook(Integer plook) {
		this.plook = plook;
	}
	@Override
	public String toString() {
		return "UserStatistics [id=" + id + ", mid=" + mid + ", eid=" + eid + ", starttime=" + starttime
				+ ", utilitytime=" + utilitytime + ", endtime=" + endtime + ", money=" + money + ", look=" + look + "]";
	}
}
